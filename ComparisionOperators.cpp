#include<iostream>
using namespace std;

int main(){

    int a= 1, b= 10;

    // Comparison operators in C++.
    // ==, !=, >, <, >=, <=
    // 1 means true and 0 means false.
    cout<<"Is a==b : "<<(a==b)<<endl; //0

    cout<<"Is a!=b : "<<(a!=b)<<endl; //1

    cout<<"Is a>b : "<<(a>b)<<endl; //0

    cout<<"Is a<b : "<<(a<b)<<endl; //1

    cout<<"Is a>=b : "<<(a>=b)<<endl; //0
    
    cout<<"Is a<=b : "<<(a<=b)<<endl; //1

    return 0;
}