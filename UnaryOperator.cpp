#include<iostream>
using namespace std;

int main(){

    // Unary Operators in C++.
    int a= 4, b= 5;
    cout<<"The value of a is : "<<a<<endl;
    cout<<"The value of b is : "<<b<<endl;

    cout<<"Now The value of a is: "<<++a<<endl;
    cout<<"Now the Value of b is: "<<--b<<endl;

    return 0;
}