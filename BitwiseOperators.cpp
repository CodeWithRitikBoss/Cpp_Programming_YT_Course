#include<iostream>
using namespace std;

int main(){

    // Bitwise operators in C++.
    // &, |, ^.

    cout<<"The bitwise AND of 4 & 4 is : "<<(4&4)<<endl; //4
    cout<<"The bitwise OR of 2 & 4 is : "<<(2|4)<<endl; //6
    cout<<"The bitwise XOR of 2 & 4 is : "<<(2^4)<<endl; //6

    return 0;
}